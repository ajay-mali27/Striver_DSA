public class J5_Whileloop {
    public static void main(String[] args) {


        // While Loop ;
        
        int i = 1;
        while (i < 5) {

        System.out.print(" Hello World ");
        i++;
        }




        // Do while Loop;
        int num = 10;

        do {
            System.out.println(num);
            num++;
        } while (num <= 5);

    }

}
